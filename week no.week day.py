weekday=int(input("enter the weekday no. between 1-7:"))
if weekday==1:
    print("Monday")
elif weekday==2:
    print("Tuesday")
elif weekday==3:
    print("Wednesday")
elif weekday==4:
    print("Thursday")
elif weekday==5:
    print("friday")
elif weekday==6:
    print("saturday")
elif weekday==7:
    print("sunday")
else:
    print("please enter the no. between 1 to 7")